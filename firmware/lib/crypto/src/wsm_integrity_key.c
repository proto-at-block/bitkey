#include "wsm_integrity_key.h"

#include "ecc.h"

// WSM Integrity Key for test/dev environment
const uint8_t WSM_INTEGRITY_TEST_PUBKEY[33] = {
  0x03, 0x07, 0x84, 0x51, 0xe0, 0xc1, 0xe1, 0x27, 0x43, 0xd2, 0xfd,
  0xd9, 0x3a, 0xe7, 0xd0, 0x3d, 0x5c, 0xf7, 0x81, 0x3d, 0x2f, 0x61,
  0x2d, 0xe1, 0x09, 0x04, 0xe1, 0xc6, 0xa0, 0xb8, 0x7f, 0x70, 0x71,
};

// WSM Integrity Key for production environment
const uint8_t WSM_INTEGRITY_PROD_PUBKEY[33] = {
  0x02, 0x95, 0x21, 0x6a, 0x2e, 0x0b, 0x54, 0xb3, 0x82, 0xcc, 0x39,
  0x38, 0xe2, 0x07, 0x29, 0x8d, 0x21, 0xcb, 0x8c, 0x5f, 0x68, 0x6f,
  0x78, 0xb0, 0x5d, 0x9f, 0x14, 0xb4, 0xe4, 0x66, 0x9e, 0x56, 0x0f,
};

bool wsm_verify_signature(const uint8_t* message, size_t message_len, const uint8_t* signature) {
  if (!message || !signature) {
    return false;
  }

#ifdef CONFIG_PROD
  const uint8_t* wsm_key = WSM_INTEGRITY_PROD_PUBKEY;
#else
  const uint8_t* wsm_key = WSM_INTEGRITY_TEST_PUBKEY;
#endif

  return crypto_ecc_secp256k1_verify_signature(wsm_key, message, message_len, signature);
}
